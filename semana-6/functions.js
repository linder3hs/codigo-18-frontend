// Function
// Calcular el area de un circulo
// Formula PI * r2

// Formar de crear una funcion

// Buena practica
function calcularAreaDeCirculo(radio) {
  const area = Math.PI * Math.pow(radio, 2);
  return Number(area.toFixed(2));
}

calcularAreaDeCirculo(10);

